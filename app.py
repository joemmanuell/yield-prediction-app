from flask import Flask, request, jsonify, render_template
import joblib

app = Flask(__name__)

# Load model and encoder
model = joblib.load('yield_prediction_model.pkl')
le = joblib.load('crop_type_encoder.pkl')

@app.route('/')
def home():
    return '''
    <h2>Farm Yield Prediction</h2>
    <form action="/predict" method="post">
        Crop Type: <input name="crop_type"><br>
        Soil Moisture: <input name="soil_moisture" type="number" step="any"><br>
        Temperature: <input name="temperature" type="number" step="any"><br>
        Humidity: <input name="humidity" type="number" step="any"><br>
        Rainfall: <input name="rainfall" type="number" step="any"><br>
        <input type="submit" value="Predict Yield">
    </form>
    '''

@app.route('/predict', methods=['POST'])
def predict():
    crop_type = request.form['crop_type']
    soil_moisture = float(request.form['soil_moisture'])
    temperature = float(request.form['temperature'])
    humidity = float(request.form['humidity'])
    rainfall = float(request.form['rainfall'])

    crop_encoded = le.transform([crop_type])[0]
    input_data = [[crop_encoded, soil_moisture, temperature, humidity, rainfall]]
    prediction = model.predict(input_data)[0]

    return f"<h3>Predicted Yield: {prediction:.2f} kg/hectare</h3>"

if __name__ == '__main__':
    app.run(debug=True)